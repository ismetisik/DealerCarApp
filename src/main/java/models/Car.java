package Models;
//pojo
public class Car {
    public int year;
    public String make;
    public String model;
    public String color;
    public int stock;
    public String image;

    public Car(int year, String make, String model, String color, String image){
        this.year=year;
        this.stock=stock;
        this.make=make;
        this.color=color;
        this.image=image;
        this.model=model;
    }
}
